package Java_Self_Practice;

public class For_NestedLoop_11111_00000_11111_5Line_PATTERN {

	public static void main(String[] args) 
	{
		for(int i=1; i<=5; i++)
		{
			for(int j=1; j<=5; j++)
			{
				if(i%2!=0)
				{
					System.out.print(1+" ");
				}
				else
					System.out.print(0+" ");
			}
			System.out.println();
		}
	}

}
