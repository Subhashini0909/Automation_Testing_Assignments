package Java_Self_Practice;

public class For_NestedLoop_12345_246810_3691215_5Line_PATTERN {

	public static void main(String[] args) 
	{
		int mul = 1;
		for(int i = 1; i<=5; i++)
		{
			for(int j = 1; j<=5; j++)
			{
				System.out.print(j*mul+ " ");
			}
			mul++;
			System.out.println();
		}
	}

}
