package Java_Self_Practice;

public class For_NestedLoop_1112131415_5Line_PATTERN {

	public static void main(String[] args) 
	{
		int count = 11;
		
		for(int i = 1; i<=5; i++)
		{
			for(int j = 1; j<=5; j++)
			{
				System.out.print(count + " ");
				count++;
			}
			System.out.println();
		}
	}

}
