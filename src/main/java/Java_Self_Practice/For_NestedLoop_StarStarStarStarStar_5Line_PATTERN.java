package Java_Self_Practice;

public class For_NestedLoop_StarStarStarStarStar_5Line_PATTERN {

	public static void main(String[] args) 
	{
		for(int i = 1; i<=5; i++)
		{
			for(int j =1; j<=5; j++)
			{
				System.out.print("* "); // ***** (Row)
			}
			System.out.println("* "); //Column
		}
	}

}
